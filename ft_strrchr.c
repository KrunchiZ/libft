#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*last;

	(char *)s;
	last = NULL;
	while (*s)
	{
		if (*s == c)
			last = s;
		s++;
	}
	if (*s == c)
		last = s;
	return (last);
}
